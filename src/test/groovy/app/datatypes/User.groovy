package app.datatypes

class User {

    String name;
    String username;
    String email
    String phone
    String website

    String getName() {
        return name
    }

    void setName(String name) {
        this.name = name
    }

    String getUsername() {
        return username
    }

    void setUsername(String username) {
        this.username = username
    }

    String getEmail() {
        return email
    }

    void setEmail(String email) {
        this.email = email
    }

    String getPhone() {
        return phone
    }

    void setPhone(String phone) {
        this.phone = phone
    }

    String getWebsite() {
        return website
    }

    void setWebsite(String website) {
        this.website = website
    }
}
